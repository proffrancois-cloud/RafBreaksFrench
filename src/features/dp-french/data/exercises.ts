const inferComponent = (seed: ExerciseSeed, skillId: string): ExamComponent => {
  if (seed.examComponent) return seed.examComponent;
  if (skillId.includes("reading")) return "Paper 2 reading";
  if (skillId.includes("listening")) return "Paper 2 listening";
  if (skillId.includes("paper1") || skillId.includes("text-type") || skillId.includes("register")) return "Paper 1 writing";
  return "Grammar accuracy";
};

